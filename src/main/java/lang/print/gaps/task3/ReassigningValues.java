package lang.print.gaps.task3;

public class ReassigningValues {
    public static void main(String[] args) {
        int first = 1;
        int second = 10;
        int third = 100;
        System.out.println(first + "\n" + second + "\n" + third);
        int linkToFirst =  15;
        int linkToSecond =  6;
        int linkToThird =  4;
        System.out.println(linkToFirst + "\n" + linkToSecond + "\n" + linkToThird);
        System.out.println(first + "\n" + second + "\n" + third);
    }
}
